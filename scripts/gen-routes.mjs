async function fetchRoute(wps) {
  const closed = [...wps, wps[0]]
  const coords = closed.map(([lng, lat]) => `${lng},${lat}`).join(';')
  const url = `${OSRM}/route/v1/driving/${coords}?overview=full&geometries=geojson&continue_straight=false`
  const res = await fetch(url, { signal: AbortSignal.timeout(30000) })
  const j = await res.json()
  if (j.code !== 'Ok') throw new Error('route ' + j.code)
  // dedupe near-identical consecutive points, store as [lat,lng]
  const raw = j.routes[0].geometry.coordinates
  const out = []
  for (const [lng, lat] of raw) {
    const p = [round(lat), round(lng)]
    const last = out[out.length - 1]
    if (!last || Math.abs(last[0] - p[0]) > 1e-5 || Math.abs(last[1] - p[1]) > 1e-5) out.push(p)
  }
  return out
}

async function fetchNearest(lng, lat) {
  const url = `${OSRM}/nearest/v1/driving/${lng},${lat}`
  const res = await fetch(url, { signal: AbortSignal.timeout(20000) })
  const j = await res.json()
  if (j.code !== 'Ok') return [round(lat), round(lng)]
  const [nlng, nlat] = j.waypoints[0].location
  return [round(nlat), round(nlng)]
}

const routes = {}
const positions = {}

for (const [id, wps] of Object.entries(LOOPS)) {
  try {
    const geom = await fetchRoute(wps)
    routes[id] = geom
    positions[id] = geom[0]
    const insideFrac = geom.filter(([lat, lng]) => inPoly([lng, lat], ZONE)).length / geom.length
    console.log(`${id}: ${geom.length} pts · inside-zone ${(insideFrac * 100).toFixed(0)}%`)
    await sleep(400)
  } catch (e) {
    console.log(`${id}: FAILED (${e.message}) — keeping hand route`)
  }
}

for (const [id, [lng, lat]] of Object.entries(STATIC)) {
  try {
    positions[id] = await fetchNearest(lng, lat)
    console.log(`${id}: snapped → ${positions[id].join(', ')}`)
    await sleep(300)
  } catch (e) {
    positions[id] = [round(lat), round(lng)]
    console.log(`${id}: nearest FAILED (${e.message})`)
  }
}

const banner = `// AUTO-GENERATED by scripts/gen-routes.mjs — DO NOT EDIT BY HAND.
// Road-snapped patrol routes & positions for Kota Manado (OSRM / OpenStreetMap).
import type { LatLng } from '../types'
`
const body =
  `\nexport const GEN_ROUTES = ${JSON.stringify(routes)} as Record<string, LatLng[]>\n` +
  `\nexport const GEN_POS = ${JSON.stringify(positions)} as Record<string, LatLng>\n`

writeFileSync('src/data/routes.generated.ts', banner + body)
