// Generates road-snapped patrol routes for each motorcycle using OSRM (OpenStreetMap
// routing). Waypoints are placed on the Manado street grid (on land); OSRM snaps them
// to the nearest roads and returns a street-following geometry. Output is baked into
// src/data/routes.generated.ts so the app needs no runtime network calls.
import { writeFileSync } from 'node:fs'

const OSRM = 'https://router.project-osrm.org'
const round = (v) => Math.round(v * 1e5) / 1e5
const sleep = (ms) => new Promise((r) => setTimeout(r, ms))

// Operational geofence (lng,lat) — for validation only.
const ZONE = [
  [124.825, 1.518], [124.865, 1.521], [124.888, 1.496], [124.87, 1.46],
  [124.825, 1.438], [124.805, 1.452], [124.806, 1.49], [124.825, 1.518],
]
function inPoly([x, y], poly) {
  let inside = false
  for (let i = 0, j = poly.length - 1; i < poly.length; j = i++) {
    const [ax, ay] = poly[i], [bx, by] = poly[j]
    if (ay > y !== by > y) {
      const xc = ((bx - ax) * (y - ay)) / (by - ay) + ax
      if (x < xc) inside = !inside
    }
  }
  return inside
}

